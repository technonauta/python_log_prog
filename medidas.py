medA = float(input("Digite a medida A: "))
medB = float(input("Digite a medida B: "))
medC = float(input("Digite a medida C: "))

QuaD = 4*medA
TriA = (medB*medA)/2
TraP = ((medA+medB)*medC)/2

print(f"ÁREA DO QUADRADO = {QuaD:.4f}")
print(f"ÁREA DO TRIÂNGULO = {TriA:.4f}")
print(f"ÁREA DO TRAPÉZIO = {TraP:.4f}")